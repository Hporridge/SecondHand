package ahualy.neepu.bean;

public class Order {

    private int order_id;
    private String shopping_name;
    private String num;
    private String money;
    private String revceiving_address;
    private String revceiving_name;
    private String revceiving_phone;
    private String send_phone;
    private String photo;
    private String orderId;

    public int getOrder_id() {
        return order_id;
    }

    public void setOrder_id(int order_id) {
        this.order_id = order_id;
    }

    public String getShopping_name() {
        return shopping_name;
    }

    public void setShopping_name(String shopping_name) {
        this.shopping_name = shopping_name;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getRevceiving_address() {
        return revceiving_address;
    }

    public void setRevceiving_address(String revceiving_address) {
        this.revceiving_address = revceiving_address;
    }

    public String getRevceiving_name() {
        return revceiving_name;
    }

    public void setRevceiving_name(String revceiving_name) {
        this.revceiving_name = revceiving_name;
    }

    public String getRevceiving_phone() {
        return revceiving_phone;
    }

    public void setRevceiving_phone(String revceiving_phone) {
        this.revceiving_phone = revceiving_phone;
    }

    public String getSend_phone() {
        return send_phone;
    }

    public void setSend_phone(String send_phone) {
        this.send_phone = send_phone;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }
}
